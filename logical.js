// Logical Operators: and, or, not ==> &&, ||, !

const x = 5;
console.log(!!x);

// and [&&] ==> Left and Right
const money = true;
console.log(money && "I will go to institute");

// or [||] ==> Left or Right
const bus = "Bus ache";
const rickshaw = "rickshaw ache";
console.log(bus || rickshaw);

// Ternary
const y = 10;
console.log(y === 5 ? "Number is 5" : "Number is not five");

